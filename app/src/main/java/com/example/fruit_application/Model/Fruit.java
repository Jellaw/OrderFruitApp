package com.example.fruit_application.Model;

public class Fruit {
    private String idFruit;
    private String name;
    private int weight;
    private int price;
    private int igm;

    public Fruit() {
    }

    public String getIdFruit() {
        return idFruit;
    }

    public void setIdFruit(String idFruit) {
        this.idFruit = idFruit;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getIgm() {
        return igm;
    }

    public void setIgm(int igm) {
        this.igm = igm;
    }
}
